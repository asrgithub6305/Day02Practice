package com.bridgelab.day02practice;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a= 10;
        int b= 5;
        int c= 2;
        System.out.println("a+b*c ="+ (a+b*c));
        System.out.println("c+a/b ="+ (c+a/b));
        System.out.println("a%b+c ="+ (a%b+c));
        System.out.println("a*b+c ="+ (a*b+c));
    }


}
